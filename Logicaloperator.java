package javaLearning.Assignments;

public class Logicaloperator {

	public static void main(String[] args)
	{
		int a=10;
		int b=20;
		if(b==b && a==10)
		{
			System.out.println("hi");
		}
		if(a==b || a==10)
		{
			System.out.println("Nisha");
		}
		if(!(a==b & a==30))
		{
			System.out.println("mishra");
		}
	

	}

}
